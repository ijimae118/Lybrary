    private void initModel() {
        //khoi tao mo hinh du lieu cho jTable tbData
        model = (DefaultTableModel) tbData.getModel();
        
        //vi du, dua vo 2 san pham
        Product p = new Product("P1000", "Coca coca", "Beverage", 13000);
        model.addRow(p.toVector());
        model.addRow(new Product("P1001","7 up","Beverage",13000).toVector());
        
        //khoi tao bo sap xep de loc du lieu cho chuc nang search
        sorter = (TableRowSorter<TableModel>) tbData.getRowSorter();
    }

    Locale loc;
    ResourceBundle rb;
    private void translate() {
        //khoi tao doi tuong Locale theo ngon ngu muon trinh bay
        if(mnEN.isSelected()){
            loc = new Locale("en", "US");
        }
        else{
            loc = new Locale("vi", "VN");
        }
        
        //khoi tao doi tuong ResourBundle de doc du lieu
        //tu cac tu dien co ngon ngu tuong ung duoc chon
        //thong qua bien locale
        rb = ResourceBundle.getBundle("dic.td",loc);
        
        //doc du lieu trong tu dien gan cho cac caption tuong ung
        this.setTitle(rb.getString("frTitle"));
        mnLang.setText(rb.getString("mnLang"));
        mnExit.setText(rb.getString("mnExit"));
        mnEN.setText(rb.getString("mnEN"));
        mnVN.setText(rb.getString("mnVN"));
        
        pnEdit.setBorder(BorderFactory.createTitledBorder(rb.getString("pnEdit")));
        lbID.setText(rb.getString("lbID"));
        lbName.setText(rb.getString("lbName"));
        
        String[]header = rb.getString("tbData").split(",");
        model.setColumnIdentifiers(header);       
    }
