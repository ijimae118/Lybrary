    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String dk1 = txtSearch2.getText().trim();
        String dk2 = rdFood.isSelected()?"Food":"Beverage";
        
        //ArrayList<Rowilter<TableModel,Object>> dk = new ArrayList<>();
        ArrayList dk = new ArrayList();
        dk.add(RowFilter.regexFilter("(?)"+dk1, 1));
        dk.add(RowFilter.regexFilter(dk2, 3));
        
        sorter.setRowFilter(RowFilter.andFilter(dk));
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataMouseClicked
        //lay chi so dong dang duoc chon
        
        int r = tbData.getSelectedRow();
        if(r<0) return;
        
        //lay du lieu cua cac cot tren dong duoc chon
        //cot thu 1
        String id = tbData.getValueAt(r, 0).toString();
        txtID.setText(id);
        
        //tuong tu cho cac con lai
        txtName.setText(tbData.getValueAt(r, 1).toString());
        
        int p = Integer.parseInt(tbData.getValueAt(r, 2).toString());
                
        txtPrice.setValue(p);
        cbCat.setSelectedItem(tbData.getValueAt(r, 3));
        
    }//GEN-LAST:event_tbDataMouseClicked

